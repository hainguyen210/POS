    public void LoadJTable(String MaHD) throws Exception{
        ChiTietHDDao cthdd = new ChiTietHDDao();
        
        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel)tblChiTietHD.getModel();
        
        ArrayList<ChiTietHD> List = cthdd.JTable(MaHD);
        for(ChiTietHD cthd:List) {
            model.addRow(new Object[]{
                cthd.getMaHD(),
                cthd.getMaHH(),
                cthd.getDonGia(),
                cthd.getSoLuong(),
                cthd.getTongTien()
            });
        }
    }
    
