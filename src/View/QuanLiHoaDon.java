    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        if (txtMaHD.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Xin vui long nhap ma hoa don");
        } else {

            try {
                //Them thong tin vao textfield

                HoaDonDao hdd = new HoaDonDao();
                HoaDon hd = hdd.TimTheoMaHD(txtMaHD.getText());

                txtNgayLap.setText(hd.getNgayLap());
                txtTongTienHD.setText(Integer.toString(hd.getTongTienHD()));

                //Truyen du lieu vao bang jtable
                LoadJTable(txtMaHD.getText());
            } catch (Exception ex) {
                java.util.logging.Logger.getLogger(QuanLiHoaDon.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

