    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (txtMaHD.getText().equals("") | txtNgayLap.getText().equals("") | txtTongTienHD.getText().equals("")) {

            JOptionPane.showMessageDialog(this, "Xin vui long chon day du thong tin");
        } else {
            try {
                HoaDonDao hdd = new HoaDonDao();
                hdd.Xoa(txtMaHD.getText());

                ChiTietHDDao cthdd = new ChiTietHDDao();
                cthdd.Xoa(txtMaHD.getText());

                txtMaHD.setText("");
                txtNgayLap.setText("");
                txtTongTienHD.setText("");
                DefaultTableModel model = new DefaultTableModel();
                model = (DefaultTableModel) tblChiTietHD.getModel();
                model.setRowCount(0);
            } catch (Exception ex) {
                java.util.logging.Logger.getLogger(QuanLiHoaDon.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

