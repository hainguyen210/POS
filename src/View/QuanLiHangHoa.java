        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel)tblHangHoa.getModel();
        ArrayList<HangHoa> listHH = hhd.Jtable();
        for(HangHoa hh:listHH){
            model.addRow(new Object[]{
                hh.getMaHH(),
                hh.getTenHH(),
                hh.getDVT(),
                hh.getDonGia(),
                hh.getSoLuong()
            });
        }
