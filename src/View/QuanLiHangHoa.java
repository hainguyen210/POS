    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed

        try {
            if (txtTenHH.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Xin vui long nhap ten hang hoa");
            } else {
                HangHoaDao hhd = new HangHoaDao();

                HangHoa hh = hhd.TimTheoTen(txtTenHH.getText());

                txtDVT.setText(hh.getDVT());
                txtMaHH.setText(hh.getMaHH());
                txtDonGia.setText(Integer.toString(hh.getDonGia()));
                txtSoLuong.setText(Integer.toString(hh.getSoLuong()));
            }
        } catch (Exception ex) {
            Logger.getLogger(QuanLiHangHoa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

