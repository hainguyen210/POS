    private void btbThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbThemMoiActionPerformed
        if (txtTenHH.getText().equals("") | txtMaHH.getText().equals("") | txtDVT.getText().equals("") | txtDonGia.getText().equals("") | txtSoLuong.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Xin vui long dien day du thong tin");
        } else {
            HangHoa hh = new HangHoa();
            HangHoaDao hhd = new HangHoaDao();

            hh.setMaHH(txtMaHH.getText());
            hh.setTenHH(txtTenHH.getText());
            hh.setDVT(txtDVT.getText());
            hh.setDonGia(Integer.parseInt(txtDonGia.getText()));
            hh.setSoLuong(Integer.parseInt(txtSoLuong.getText()));

            try {
                hhd.ThemMoi(hh);
            } catch (Exception ex) {
                Logger.getLogger(QuanLiHangHoa.class.getName()).log(Level.SEVERE, null, ex);
            }

            txtTenHH.setText("");
            txtMaHH.setText("");
            txtDVT.setText("");
            txtDonGia.setText("");
            txtSoLuong.setText("");
            DefaultTableModel model = new DefaultTableModel();
            model = (DefaultTableModel) tblHangHoa.getModel();
            model.setRowCount(0);
            try {
                LoadJtable();
            } catch (Exception ex) {
                Logger.getLogger(QuanLiHangHoa.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btbThemMoiActionPerformed

